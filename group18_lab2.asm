; 在RAM中，位址為0x10~0x1f設定七段顯示器對應的值
mov 0x10, #0xC0
mov 0x11, #0xF9
mov 0x12, #0xA4
mov 0x13, #0xB0
mov 0x14, #0x99
mov 0x15, #0x92
mov 0x16, #0x82
mov 0x17, #0xF8
mov 0x18, #0x80
mov 0x19, #0x90
mov 0x1a, #0x88
mov 0x1b, #0x83
mov 0x1c, #0xC6
mov 0x1d, #0xA1
mov 0x1e, #0x86
mov 0x1f, #0x8E

; A存的是位址
; 設定A的初始值為第一個位址0x10
mov A, #0x10
main:
  ; 把ACC對應記憶體位置所存的值放入七段顯示器
  mov R1, A
  mov P1, @R1

  ; 判斷使用者是否按下Switch0，若沒按下則正向顯示
  jb P2.0, up

  ; 反向顯示
  down:
    dec A
    ; 邊緣判斷，若還沒減到0x10以下就回到main，否則重置ACC為0x1F
    cjne A, #0x0F, main
    mov A, #0x1F
    jmp main

  ; 正向顯示
  up:
    inc A
    ; 邊緣判斷，若還沒加到0x1F以上就回到main，否則重置ACC為0x10
    cjne A, #0x20, main
    mov A, #0x10
    jmp main